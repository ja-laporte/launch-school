# What's the difference between an Array and a Hash?

<<-DIFFERENCE

An array is a collection of items in a list. The list has a specific order in that it uses index numbers to reference the list items. The index begins at 0. Where that differs from a hash is that a Hash is a collection of items in a key-value pair model. A hash's order of key-value pairs don't matter, as you can locate the items using either the key or the value. An Array requires you to use the index number of the element item in order to retrieve/manipulate/delete/etc., the element.

Lastly, they are different in their syntax: An Array is created using square brackets [], while a Hash is created using curly braces {} . 


